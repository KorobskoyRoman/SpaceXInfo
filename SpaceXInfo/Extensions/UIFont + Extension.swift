import Foundation
import UIKit

extension UIFont {
    static func sfPro16() -> UIFont? {
        return UIFont.init(name: "SF Pro", size: 16)
    }
    
    static func sfPro20() -> UIFont? {
        return UIFont.init(name: "SF Pro", size: 20)
    }
}
